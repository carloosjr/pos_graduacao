# Que tal uma brincadeira para aplicar com os amigos
# e familiares?

# Vamos desenvolver um programa que solicite o “seu
# nome”. Enquanto a pessoa não digitar o “seu nome” o
# programa não irá terminar.

while True:
    nome = input("Informe seu nome: \n")
    if nome.upper() == 'seu nome'.upper():
        print("Acertou mizeraviiiiiiiii")
        break
    
